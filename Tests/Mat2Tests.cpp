#include <cassert>
#include <cmath>
#include <iostream>

#include "../Core/Math/Mat2.h"

int main()
{
    std::cout << "========== Mat2 Tests ==========\n";

    // ==========================================================
    // Constructors
    // ==========================================================

    Mat2 A;

    assert(A.m00 == 0.0f);
    assert(A.m01 == 0.0f);
    assert(A.m10 == 0.0f);
    assert(A.m11 == 0.0f);

    Mat2 B(1, 2,
           3, 4);

    assert(B.m00 == 1.0f);
    assert(B.m01 == 2.0f);
    assert(B.m10 == 3.0f);
    assert(B.m11 == 4.0f);

    // ==========================================================
    // Identity
    // ==========================================================

    Mat2 I = Mat2::Identity();

    assert(I == Mat2(1,0,
                     0,1));

    // ==========================================================
    // Zero
    // ==========================================================

    Mat2 Z = Mat2::Zero();

    assert(Z == Mat2(0,0,
                     0,0));

    // ==========================================================
    // Addition
    // ==========================================================

    Mat2 C = B + B;

    assert(C == Mat2(2,4,
                     6,8));

    // ==========================================================
    // Subtraction
    // ==========================================================

    C = B - Mat2(1,1,
                 1,1);

    assert(C == Mat2(0,1,
                     2,3));

    // ==========================================================
    // Scalar Multiplication
    // ==========================================================

    C = B * 2.0f;

    assert(C == Mat2(2,4,
                     6,8));

    // ==========================================================
    // Scalar Division
    // ==========================================================

    C /= 2.0f;

    assert(C == B);

    // ==========================================================
    // +=
    // ==========================================================

    C = B;

    C += B;

    assert(C == Mat2(2,4,
                     6,8));

    // ==========================================================
    // -=
    // ==========================================================

    C -= B;

    assert(C == B);

    // ==========================================================
    // *=
    // ==========================================================

    C *= 3.0f;

    assert(C == Mat2(3,6,
                     9,12));

    // ==========================================================
    // /=
    // ==========================================================

    C /= 3.0f;

    assert(C == B);

    // ==========================================================
    // Matrix Multiplication
    // ==========================================================

    Mat2 D(5,6,
           7,8);

    C = B * D;

    assert(C == Mat2(
        19,22,
        43,50
    ));

    // ==========================================================
    // Matrix × Vector
    // ==========================================================

    Vec2 V(2,3);

    Vec2 R = B * V;

    assert(R == Vec2(
        8,
        18
    ));

    // ==========================================================
    // Determinant
    // ==========================================================

    assert(B.Determinant() == -2.0f);

    // ==========================================================
    // Trace
    // ==========================================================

    assert(B.Trace() == 5.0f);

    // ==========================================================
    // Transposed
    // ==========================================================

    C = B.Transposed();

    assert(C == Mat2(
        1,3,
        2,4
    ));

    // ==========================================================
    // Transpose
    // ==========================================================

    Mat2 E = B;

    E.Transpose();

    assert(E == Mat2(
        1,3,
        2,4
    ));

    // ==========================================================
    // Inverse
    // ==========================================================

    Mat2 Inv = B.Inverse();

    assert(std::fabs(Inv.m00 - (-2.0f)) < 0.0001f);
    assert(std::fabs(Inv.m01 - ( 1.0f)) < 0.0001f);
    assert(std::fabs(Inv.m10 - ( 1.5f)) < 0.0001f);
    assert(std::fabs(Inv.m11 - (-0.5f)) < 0.0001f);

    // ==========================================================
    // Invert
    // ==========================================================

    E = B;

    assert(E.Invert());

    assert(std::fabs(E.m00 - (-2.0f)) < 0.0001f);
    assert(std::fabs(E.m01 - ( 1.0f)) < 0.0001f);
    assert(std::fabs(E.m10 - ( 1.5f)) < 0.0001f);
    assert(std::fabs(E.m11 - (-0.5f)) < 0.0001f);

    // ==========================================================
    // IsIdentity
    // ==========================================================

    assert(I.IsIdentity());

    // ==========================================================
    // IsZero
    // ==========================================================

    assert(Z.IsZero());

    // ==========================================================
    // Equality
    // ==========================================================

    assert(B == B);

    // ==========================================================
    // Inequality
    // ==========================================================

    assert(B != D);

    std::cout << "All Mat2 Tests Passed!\n";

    return 0;
}