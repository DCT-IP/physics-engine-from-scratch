#include <cassert>
#include <iostream>

#include "../Core/Math/MathUtils.h"

int main()
{
    std::cout << "========== MathUtils Tests ==========\n";

    // ==========================================================
    // Constants
    // ==========================================================

    assert(Math::PI > 3.14f);
    assert(Math::TWO_PI > 6.28f);
    assert(Math::HALF_PI > 1.57f);

    // ==========================================================
    // NearlyEqual
    // ==========================================================

    assert(Math::NearlyEqual(5.0f, 5.0f));
    assert(Math::NearlyEqual(1.0000001f, 1.0000002f));
    assert(!Math::NearlyEqual(1.0f, 2.0f));

    // ==========================================================
    // Clamp
    // ==========================================================

    assert(Math::Clamp(5.0f, 0.0f, 10.0f) == 5.0f);
    assert(Math::Clamp(-5.0f, 0.0f, 10.0f) == 0.0f);
    assert(Math::Clamp(20.0f, 0.0f, 10.0f) == 10.0f);

    // ==========================================================
    // Lerp
    // ==========================================================

    assert(Math::Lerp(0.0f, 10.0f, 0.0f) == 0.0f);
    assert(Math::Lerp(0.0f, 10.0f, 0.5f) == 5.0f);
    assert(Math::Lerp(0.0f, 10.0f, 1.0f) == 10.0f);

    // ==========================================================
    // Min / Max
    // ==========================================================

    assert(Math::Min(5.0f, 10.0f) == 5.0f);
    assert(Math::Max(5.0f, 10.0f) == 10.0f);

    // ==========================================================
    // Abs
    // ==========================================================

    assert(Math::Abs(-5.0f) == 5.0f);
    assert(Math::Abs(5.0f) == 5.0f);

    // ==========================================================
    // Sign
    // ==========================================================

    assert(Math::Sign(10.0f) == 1.0f);
    assert(Math::Sign(-10.0f) == -1.0f);
    assert(Math::Sign(0.0f) == 0.0f);

    // ==========================================================
    // Degrees <-> Radians
    // ==========================================================

    assert(Math::NearlyEqual(
        Math::DegreesToRadians(180.0f),
        Math::PI));

    assert(Math::NearlyEqual(
        Math::RadiansToDegrees(Math::PI),
        180.0f));

    // ==========================================================
    // Square
    // ==========================================================

    assert(Math::Square(5.0f) == 25.0f);

    // ==========================================================
    // Cube
    // ==========================================================

    assert(Math::Cube(3.0f) == 27.0f);

    // ==========================================================
    // IsZero
    // ==========================================================

    assert(Math::IsZero(0.0f));
    assert(Math::IsZero(0.0000001f));
    assert(!Math::IsZero(5.0f));

    // ==========================================================
    // IsFinite
    // ==========================================================

    assert(Math::IsFinite(5.0f));

    std::cout << "All MathUtils Tests Passed!\n";

    return 0;
}